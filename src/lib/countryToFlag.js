/**
 * @param {string} location
 * @returns {string | null}
 */
function getCountryCode(location) {
  if (!location) return null;
  const trimmed = location.trim();
  // Take last segment (typically country in "City, Country" or "City, State, Country")
  const parts = trimmed.split(/[,;]/).map((p) => p.trim());
  const lastPart = parts[parts.length - 1];
  if (!lastPart) return null;
  // If last part is a 2-letter code (e.g. "DE", "US")
  if (lastPart.length === 2 && /^[a-zA-Z]{2}$/.test(lastPart)) {
    return NAME_TO_CODE[lastPart.toLowerCase()] ?? lastPart.toUpperCase();
  }
  const key = lastPart.toLowerCase().replace(/\s+/g, " ");
  return NAME_TO_CODE[key] ?? null;
}
