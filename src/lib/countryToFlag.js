/**
 * Get a flag emoji for a location string if it contains a recognized country.
 * Supports formats like "Berlin, Germany", "Australia", "London, UK".
 *
 * @param {string} location - User-entered location string
 * @returns {string | null} - Flag emoji or null if no match
 */
export function getCountryFlag(location) {
  if (!location || typeof location !== "string") return null;
  const code = getCountryCode(location.trim());
  if (!code) return null;
  return codeToFlag(code);
}

/**
 * @param {string} code - ISO 3166-1 alpha-2 country code
 * @returns {string} - Flag emoji
 */
function codeToFlag(code) {
  if (code.length !== 2) return "";
  return String.fromCodePoint(
    ...code
      .toUpperCase()
      .split("")
      .map((c) => 127397 + c.charCodeAt(0))
  );
}

